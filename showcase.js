
function changeImage() {
    var image = document.getElementById('display1');
    if (image.src.match("img/gallery/display1.jpg")) {
        image.src = "img/gallery/display1m.jpg";
        image.style.setProperty("height", "320px", "important");
    }
    else {
        image.src = "img/gallery/display1.jpg";
    }
}
